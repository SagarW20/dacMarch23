

public class Conversion30 {
    public static void main(String args[]){
        double d1 = 962860862.962;
        double d2 = 82387457922.72;

        //minimum value
        System.out.println("Minimum value: "+Double.min(d1, d2));

        //maximum value
        System.out.println("Maximum value: "+Double.max(d1, d2));

        //sum
        System.out.println("Sum: "+Double.sum(d1, d2));
    }
}
