public class Conversion26 {
    public static void main(String args[]){
        float f1 = 6728.87f;
        float f2 = 7273.64f;

        //minimum number 
        System.out.println(Float.min(f1, f2));

        //maximum number 
        System.out.println(Float.max(f1, f2));

        //sum
        System.out.println(Float.sum(f1, f2));
    }
}
