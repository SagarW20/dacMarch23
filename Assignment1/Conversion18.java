public class Conversion18 {
    public static void main(String args[]){
        int a = 5234;
        int b = 8236;

        //finding minimum
        System.out.println("Minimum value: "+Integer.min(a,b));

        //finding maximum
        System.out.println("Maximum value: "+Integer.max(a,b));

        //sum
        System.out.println("Sum: "+Integer.sum(a,b));

    }
}
