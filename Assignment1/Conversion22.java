public class Conversion22 {
    public static void main(String[] args){
        long l1 = 3949l;
        long l2 = 86934591l;

        //minimum value 
        System.out.println("Minimum value: "+Long.min(l1, l2));

        //maximum value 
        System.out.println("Maximum value: "+Long.max(l1, l2));

        //sum 
        System.out.println("Sum: "+Long.sum(l1, l2));
    }
}
