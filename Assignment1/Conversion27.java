public class Conversion27 {
    public static void main(String args[]){
        //number of bits used to represent a double value
        System.out.println("bits size"+ Double.SIZE);

        //number of bytes used to represent a double value
        System.out.println("byte size"+ Double.BYTES);

        //Minimum double value
        System.out.println("Minimum value"+ Double.MIN_VALUE);

        //Maximum double value
        System.out.println("Maximum value"+ Double.MAX_VALUE);
    }
}
